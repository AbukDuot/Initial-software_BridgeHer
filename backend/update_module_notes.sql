## What is a Business Pitch?
A pitch is a brief, persuasive presentation of your business idea designed to convince others (investors, customers, partners) to support you.

## Types of Pitches

### 1. Elevator Pitch (30-60 seconds)
Quick overview you can deliver in an elevator ride.

**Structure:**
- Who you are
- What you do
- Who you serve
- What makes you different
- Your ask

**Example:**
"I''m Akuol, and I run Fresh Harvest, a business delivering organic vegetables to busy families in Juba. Unlike supermarkets, we source directly from local farmers, ensuring freshness and supporting our community. We''re looking for 50 new customers this month."

### 2. Short Pitch (2-5 minutes)
More detailed, for networking events or initial meetings.

### 3. Full Presentation (10-20 minutes)
Comprehensive pitch for investors or major partners.

## The Perfect Pitch Structure

### 1. The Hook (30 seconds)
Grab attention immediately!

**Options:**
- Surprising statistic
- Compelling question
- Personal story
- Bold statement

**Example:**
"Did you know that 60% of families in Juba struggle to find fresh vegetables? I did too, until I started Fresh Harvest."

### 2. The Problem (1 minute)
